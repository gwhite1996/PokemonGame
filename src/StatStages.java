
public class StatStages{
	int attack;
	int deffense;
	int spAtk;
	int spDef;
	int speed;
	int accuracy;
	int evasion;
	
	public StatStages(){
		attack = 0;
		deffense = 0;
		spAtk = 0;
		spDef = 0;
		speed = 0;
		accuracy = 0;
		evasion = 0;
	}
	
	public void resetAll(){
		attack = 0;
		deffense = 0;
		spAtk = 0;
		spDef = 0;
		speed = 0;
		accuracy = 0;
		evasion = 0;
	}
}
